@patch("atelier.durability.bigquery_ml_helper._bq_enabled")
@patch("atelier.durability.bigquery_ml_helper.bigquery")
def test_query_telemetry_conversational_unsafe_queries(mock_bq, mock_enabled):
    """Verify that unsafe queries generated by the LLM are blocked and not executed."""
    mock_enabled.return_value = True

    mock_client = MagicMock()
    mock_bq.Client.return_value = mock_client

    helper = BigQueryMLHelper(project_id="my-project")

    # Case 1: Non-SELECT query
    mock_translation_job = MagicMock()
    mock_translation_job.result.return_value = [
        {"ml_generate_text_result": "DELETE FROM trajectories WHERE tenant_id = 'tenant-y'"}
    ]
    mock_client.query.return_value = mock_translation_job

    results = helper.query_telemetry_conversational("Delete all data")
    assert results == []
    assert mock_client.query.call_count == 1

    mock_client.reset_mock()

    # Case 2: Query containing forbidden keyword
    mock_translation_job.result.return_value = [
        {"ml_generate_text_result": "SELECT * FROM trajectories; DROP TABLE trajectories;"}
    ]
    results = helper.query_telemetry_conversational("Drop table")
    assert results == []
    assert mock_client.query.call_count == 1
